    // ─────────────────────────────── Deck ───────────────────────────────
    private sealed class ZonaDeck
    {
        private readonly string[] _source;
        private readonly int[] _order;
        private int _pos;

        public ZonaDeck(string[] source)
        {
            _source = source;
            _order  = Enumerable.Range(0, source.Length).ToArray();
            _pos    = source.Length; // вынуждает перемешать при первом обращении
        }

        public string Next()
        {
            if (_pos >= _order.Length)
            {
                var last = _pos > 0 ? _order[_order.Length - 1] : -1;
                Shuffle();
                // исключаем повтор последнего сообщения при повторном цикле
                if (_order.Length > 1 && _order[0] == last)
                    (_order[0], _order[1]) = (_order[1], _order[0]);
                _pos = 0;
            }
            return _source[_order[_pos++]];
        }

        private void Shuffle()
        {
            for (var i = _order.Length - 1; i > 0; i--)
            {
                var j = Random.Shared.Next(i + 1);
                (_order[i], _order[j]) = (_order[j], _order[i]);
            }
        }
    }
